void PrintImage(int[,]image)
{
   for (int i = 0; i < image.GetLength(0); i++ ) // по строкам либо можно использовать для поиска "matrix.GetLength(0)"
   {
    for (int j = 0 ; j < image.GetLength(1); j ++) // внутренний по столбцам либо можно использовать для поиска "matrix.GetLength(1)"
    {
        //   Console.Write ($"{image[i, j]} ");
        if(image[i,j]==0) Console.Write($" ");
        else Console.Write($"+");
    }
   Console.WriteLine();
   }
}

// закрашиваем 
void FillImage(int row,int col )
{
    if (pic[row, col]== 0)
    {
        pic[row, col]= 1;
        FillImage(row-1, col ); // на строчку выше
        FillImage(row ,col-1 );//влево
        FillImage(row+1 ,col );// вниз
        FillImage(row ,col+1 );//вправо
    }
}

